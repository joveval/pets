CREATE TABLE DIRECCION(
	ID_DIRECCION INTEGER NOT NULL PRIMARY KEY,
    ID_PERSONA INTEGER NOT NULL REFERENCES PERSONA(ID_PERSONA),
    NOMBRE_DIRECCION VARCHAR(500),
    DISTRITO VARCHAR(50),
    PROVINCIA VARCHAR(50),
    DEPARTAMENTO VARCHAR(50),
    UBIGEO VARCHAR(6),
    COORD_X real,
    COORD_Y real
);